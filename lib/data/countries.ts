  "Seed/mock profile for product architecture. Treat figures, participants and regulatory notes as directional and verify against official sources before use."

function makeSources(slug: string, systemName: string, regulator?: string): SourceReference[] {
  return [
    {
      id: `${slug}-source-system`,
      title: `${systemName} official system page placeholder`,
      url: `https://example.com/sources/${slug}/official-system`,
      sourceType: "official_system_site",
      reliability: "medium",
      lastChecked: "2026-05-04",
      notes: "Placeholder URL. Replace with the official operator source during real ingestion.",
    },
    {
      id: `${slug}-source-regulator`,
      title: `${regulator ?? "Central bank / regulator"} reference placeholder`,
      url: `https://example.com/sources/${slug}/regulator`,
      sourceType: "central_bank",
      reliability: "medium",
      lastChecked: "2026-05-04",
      notes: "Seed source slot reserved for central bank, regulator or government references.",
    },
    {
      id: `${slug}-source-media`,
      title: `${systemName} fintech coverage placeholder`,
      url: `https://example.com/sources/${slug}/market-coverage`,
      sourceType: "fintech_media",
      reliability: "low",
      lastChecked: "2026-05-04",
      notes: "Media-source placeholder for market coverage, merchant rollout and PSP access changes.",
    },
  ]
}

function makeNews(slug: string, topics: SeedCountryInput["newsTopics"]): NewsItem[] {
  const dates = ["2026-05-04", "2026-05-02", "2026-04-29", "2026-04-22", "2026-04-15"]
  return topics.slice(0, 5).map((topic, index) => ({
    id: `${slug}-news-${index + 1}`,
    countrySlug: slug,
    date: dates[index] ?? "2026-04-10",
    sourceUrl: `https://example.com/news/${slug}/${index + 1}`,
    confidence: index === 0 ? "medium" : "low",
    parsedAt: "2026-05-05T06:00:00Z",
    relatedCountries: [],
    relatedSystems: [],
    ...topic,
    summary: `${topic.summary} ${seedNotice}`,
  }))
}

function makeCountry(input: SeedCountryInput): CountryPaymentProfile {
  const system: LocalPaymentSystem = {
    id: `${input.slug}-system-primary`,
    name: input.system.name,
    localBrandName: input.system.localBrandName,
    type: input.system.type,
    operator: input.system.operator,
    regulator: input.system.regulator,
    launchYear: input.system.launchYear,
    status: input.system.status,
    qrStandard: input.system.qrStandard ?? "unknown",
    qrModes: input.system.qrModes ?? [],
    useCases: input.useCases,
    accessModel: input.accessModel ?? "Requires verification; likely depends on local licensing, bank sponsorship or approved PSP/acquirer relationships.",
    settlementModel: input.settlementModel ?? "Seed assumption: domestic local-currency settlement; cross-border FX flow requires verification.",
    documentationUrls: [`https://example.com/docs/${input.slug}`],
    notes: seedNotice,
    confidence: input.confidence ?? "medium",
  }

  const sources = makeSources(input.slug, input.system.name, input.system.regulator)
  const news = makeNews(input.slug, input.newsTopics)

  return {
    id: input.slug,
    slug: input.slug,
    countryName: input.countryName,
    iso2: input.iso2,
    iso3: input.iso3,
    flagEmoji: input.flagEmoji,
    region: input.region,
    subregion: input.subregion,
    coordinates: input.coordinates,
    currency: input.currency,
    localSystems: [system],
    cryptoRegulation: {
      legalStatus: input.crypto.legalStatus,
      cryptoPaymentsAllowed: input.crypto.cryptoPaymentsAllowed,
      exchangesAllowed: input.crypto.exchangesAllowed,
      stablecoinStatus: input.crypto.stablecoinStatus ?? "unclear",
      cbdcStatus: input.crypto.cbdcStatus ?? "unknown",
      regulator: input.crypto.regulator,
      amlKycNotes: "Seed note: AML/KYC treatment should be verified against regulator guidance and licensed exchange rules.",
      taxNotes: "Seed note: tax treatment not confirmed in this MVP dataset.",
      riskSummary: `${input.crypto.riskSummary ?? "Requires verification."} ${seedNotice}`,
      lastUpdated: "2026-05-04",
      confidence: input.confidence ?? "medium",
      sources: sources.map((source) => source.url),
    },
    banksAndPsps: input.participants.map((participant, index) => ({
      id: `${input.slug}-participant-${index + 1}`,
      lastVerified: "2026-05-04",
      ...participant,
    })),
    merchantCoverage: {
      level: input.merchantLevel,
      merchantCategories: input.merchantCategories,
      knownChains: input.knownChains ?? [],
      qrTypes: input.qrTypes,
      onlineAcceptance: input.useCases.includes("e-commerce") ? true : "unknown",
      offlineAcceptance: input.useCases.includes("retail POS") ? true : "unknown",
      notes: seedNotice,
      confidence: input.confidence ?? "medium",
    },
    interoperability: input.corridors.map((corridor, index) => ({
      id: `${input.slug}-corridor-${index + 1}`,
      sourceCountry: corridor.sourceCountry ?? input.countryName,
      sources: corridor.sources ?? [`https://example.com/corridors/${input.slug}/${index + 1}`],
      ...corridor,
    })),
    news,
    sources,
    pspOpportunity: input.opportunity,
    dataQuality: {
      lastUpdated: "2026-05-04",
      lastSourceCheck: "2026-05-04",
      numberOfSources: sources.length,
      confidenceByCategory: {
        qrSystem: input.confidence ?? "medium",
        banksPsps: "low",
        interoperability: input.corridors.length ? input.confidence ?? "medium" : "low",
        merchantCoverage: input.confidence ?? "medium",
        cryptoRegulation: "low",
        news: "low",
      },
      warnings: [
        "Seed/mock data: verify before commercial decisions.",
        "Official source links are placeholders in this MVP.",
        "Merchant counts and exact participant coverage are intentionally not asserted.",
      ],
    },
  }
}

